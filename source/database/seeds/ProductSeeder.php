    public function run()
    {
        DB::table('products')->truncate();
        foreach ($this->btd as $kp => $bt)
        {
            foreach ($bt as $pd){

                $product = new Product();
                $product->name = $pd[0];
                $product->price = $pd[1] > 1 ? $pd[1] : -1;
                $product->thumbnail = $pd[2] ? '/upload/cate'.$kp.'/' .  $pd[2] : "";
                $product->image1 = $pd[3] ? '/upload/cate'.$kp.'/' . $pd[3] : '';
                $product->image2 = $pd[4] ? '/upload/cate'.$kp.'/' . $pd[4] : '';
                $product->image3 = $pd[5] ? '/upload/cate'.$kp.'/' . $pd[5] : '';
                $product->descriptionTitle = $pd[6] ? : "";
                $product->descriptionContent = $pd[7] ? : "";
                $product->categoryId = ($kp == 6 || $kp == 8) ? 4 : $kp;
                $product->rateTimes = 1;
                $product->ratePoints = 5;
                $product->save();
            }
        }

    }
